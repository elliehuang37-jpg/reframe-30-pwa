/* ---- 工廠函式：標準的「單一模式」日 ---- */
function dayPattern(day, week, title){
  // day 2 → pattern 1, day 3 → pattern 2 ... 對應第一/二/三週
  const map = {2:1,3:2,4:3,5:4,6:5, 8:6,9:7,10:8,11:9,12:10,13:11, 15:12,16:13,17:14};
  const p = P(map[day]);
  return {
    day, week, type: "pattern", patternId: p.id,
    title: `第 ${p.id} 式 · ${p.zh}（${p.en}）`,
    subtitle: title,
    concept: `${p.detail}\n\n【一句話心法】${p.oneLine}`,
    stems: p.stems,
    example: { belief: MASTER_BELIEF, reframe: p.bookReframe,
      note: "上面是書中對 master 範例的示範。下面，換成你自己的信念來練。" },
    practice: {
      instruction: `用「${p.zh}」這一式，回應你在第 1 天寫下的信念。可參考上方句型起手。`,
      placeholder: `用『${p.zh}』回應：…`
    },
    challenge: `今天留意一次身邊的抱怨，在心裡用『${p.zh}』默默回應一遍。`
  };
}

